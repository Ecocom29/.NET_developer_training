namespace AbstractClass_Middle_Level
{
    public abstract class Animal
    {
        public void Eat()
        {
            Console.WriteLine("The animal wants to eat...");
        }

        public void Run()
        {
            Console.WriteLine("The animal wants to run...");
        }

        public void Walk()
        {
            Console.WriteLine("The animal wants to walk...");
        }
    }
}
